# This Python script showcases various data types and demonstrates the usage of comments effectively.

# Data Types:
age = 25  # 🎂 Integer representing age
height = 1.75  # 📏 Float representing height in meters
name = "Alice"  # 👩‍💼 String representing name
is_student = True  # 🎓 Boolean representing student status
fruits = ["apple", "banana", "orange"]  # 🍎🍌🍊 List of favorite fruits
coordinates = (10, 20)  # 📍 Tuple representing coordinates
person = {"name": "Bob", "age": 30, "city": "New York"}  # 👤 Dictionary representing person
colors = {"red", "green", "blue"}  # 🌈 Set of colors

# Print data types
print("Age:", age)
print("Height:", height)
print("Name:", name)
print("Is student?", is_student)
print("Fruits:", fruits)
print("Coordinates:", coordinates)
print("Person:", person)
print("Colors:", colors)

